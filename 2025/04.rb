require "rmagick"
@frames = Magick::ImageList.new

def draw_frame(diagram)
  return unless ENV["VISUALIZE"]

  img = Magick::Image.new(diagram[0].size, diagram.size)

  diagram.each_with_index do |row, i|
    row.each_with_index do |cell, j|
      color = cell == "ğŸ§»" ? "white" : "black"
      img.pixel_color(j, i, color)
    end
  end

  @frames << img
end

def make_gif
  return unless ENV["VISUALIZE"]

  image_name = "04.gif"
  @frames.write(image_name)
  `gifsicle --scale=8 --optimize=3 --lossy=200 --delay=1 --loopcount=0 --threads=4 --output=#{image_name} #{image_name}`
end

def solve(diagram, iterate)
  valid = 0

  loop do
    valid_arr = []

    diagram.each_with_index do |row, i|
      valid_arr << []

      row.each_with_index do |cell, j|
        valid_arr[i][j] = cell

        next unless cell == "ğŸ§»"
        neighbours = 0

        if i > 0
          neighbours += 1 if j > 0 && diagram[i - 1][j - 1] == "ğŸ§»"
          neighbours += 1 if diagram[i - 1][j] == "ğŸ§»"
          neighbours += 1 if j < row.size - 1 && diagram[i - 1][j + 1] == "ğŸ§»"
        end

        neighbours += 1 if j > 0 && diagram[i][j - 1] == "ğŸ§»"
        neighbours += 1 if j < row.size - 1 && diagram[i][j + 1] == "ğŸ§»"

        if i < diagram.size - 1
          neighbours += 1 if j > 0 && diagram[i + 1][j - 1] == "ğŸ§»"
          neighbours += 1 if diagram[i + 1][j] == "ğŸ§»"
          neighbours += 1 if j < row.size - 1 && diagram[i + 1][j + 1] == "ğŸ§»"
        end

        if neighbours < 4
          valid += 1
          valid_arr[i][j] = "x"
        end
      end
    end

    break unless iterate
    break if valid_arr.flatten.none? { |c| c == "x" }

    draw_frame(diagram)

    diagram = valid_arr.map { |row| row.map { |cell| cell == "x" ? "â¬›ï¸" : cell } }
  end

  draw_frame(diagram)

  valid
end

puts "Part 1: #{solve(diagram, false)}"
puts "Part 2: #{solve(diagram, true)}"
make_gif

