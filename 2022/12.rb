require "set"

class Node
  attr_accessor :x, :y, :value

  def initialize(x, y, value)
    @x = x
    @y = y

    @value = case value
    when "a".."z"
      value.ord
    when "S"
      "a".ord
    when "E"
      "z".ord
    else
      throw "TILT"
    end
  end

  def valid?(other)
    other.value - value <= 1
  end
end

