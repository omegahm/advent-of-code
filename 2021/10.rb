def first_incorrect(line)
  open_braces = []

  line.chars.each do |char|
    if '([{<'.include?(char)
      open_braces << char
    else
      last_open = open_braces[-1]
      case "#{last_open}#{char}"
      when '()', '[]', '{}', '<>'
        open_braces.pop
      else
        # Early return for the first incorrect brace
        return char
      end
    end
  end

  nil
end

