# PART 2
PART2_SCORES = { '(' => 1, '[' => 2, '{' => 3, '<' => 4 }.freeze
def fix_line(line)
  # Removing all correctly balanced braces we are left with the opening braces of the missing ones
  while line.include?('()') || line.include?('[]') || line.include?('{}') || line.include?('<>')
    line.gsub!('()', '')
    line.gsub!('[]', '')
    line.gsub!('{}', '')
    line.gsub!('<>', '')
  end

  line.chars.reverse.reduce(0) do |result, char|
    result *= 5
    result += PART2_SCORES[char]
  end
end

incomplete_lines = data.lines.map(&:chomp).reject(&method(:first_incorrect)).map(&method(:fix_line))

puts incomplete_lines.sort[incomplete_lines.size/2]
