twos = 0
threes = 0

input.split("\n").each do |line|
  twos += 1 if line.chars.tally.values.include?(2)
  threes += 1 if line.chars.tally.values.include?(3)
end

puts "Part 1: #{twos * threes}"

def hamming_distance(s1, s2)
  s1.chars.zip(s2.chars).count { |a, b| a != b }
end

x, y = input.split("\n").combination(2).find do |a, b|
  hamming_distance(a, b) == 1
end

result = x.chars.zip(y.chars).reject { |a, b| a != b }.map(&:first).join
puts "Part 2: #{result}"
